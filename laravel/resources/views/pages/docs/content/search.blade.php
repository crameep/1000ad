<h3>Searching Players</h3>
<p>Find other players in the game world to scout potential targets, allies, or trade partners.</p>

<h4>Search Methods</h4>
<ul>
    <li><b>By Player Number</b> - Look up a specific player by their unique ID</li>
    <li><b>By Name</b> - Search for players by their empire name</li>
    <li><b>By Alliance</b> - Browse all members of a specific alliance</li>
    <li><b>By Online Status</b> - Find players who are currently active</li>
</ul>

<h4>Player Information</h4>
<p>Search results show basic public information about each player, including their empire name, alliance, and score. For detailed intelligence, send a <b>thief</b> on a steal info mission.</p>
