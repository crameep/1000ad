<h3>Empire Management</h3>
<p>Fine-tune your empire's operations through weapon production, food rationing, and land conversion.</p>

<h4>Weapon Production</h4>
<p>Allocate your weapon smiths across different weapon types:</p>
<ul>
    <li><b>Bows</b> - Percentage of smiths producing bows for archers</li>
    <li><b>Swords</b> - Percentage of smiths producing swords for swordsmen and horsemen</li>
    <li><b>Maces</b> - Percentage of smiths producing maces for macemen</li>
</ul>
<p>Allocations must total 100%. Adjust based on which units you plan to train.</p>

<h4>Food Rationing</h4>
<p>Set your food rationing from <b>Very Low</b> to <b>Very High</b> across 7 levels. Higher rationing increases population growth but consumes more food per turn. Balance growth needs against food supply.</p>

<h4>Land Conversion</h4>
<p>Convert land from one type to another:</p>
<ul>
    <li><b>Mountain to Forest</b> - Costs <b>100 gold</b> per acre</li>
    <li><b>Forest to Plains</b> - Costs <b>25 gold</b> per acre</li>
</ul>
<p>Land conversion is one-way (mountain &rarr; forest &rarr; plains). Plan your land distribution carefully as you cannot convert plains back to forest or mountain.</p>
