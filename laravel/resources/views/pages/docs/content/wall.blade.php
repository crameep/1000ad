<h3>Great Wall</h3>
<p>Build a great wall around your empire to increase your defensive strength against attacks.</p>

<h4>Defense Bonus</h4>
<p>The wall provides a <b>defense bonus of up to 100%</b>, effectively doubling your defensive power. The bonus scales with how much wall you have relative to your total land.</p>

<h4>Wall Requirements</h4>
<ul>
    <li>You need wall units equal to <b>5% of your total land</b> for maximum bonus</li>
    <li>Partial wall construction provides proportional defense</li>
    <li>As your land grows, you need to build more wall to maintain the bonus</li>
</ul>

<h4>Construction Costs</h4>
<p>Each wall unit costs:</p>
<ul>
    <li><b>Gold</b></li>
    <li><b>Wood</b></li>
    <li><b>Iron</b></li>
    <li><b>Wine</b></li>
</ul>

<h4>Builders</h4>
<p>Dedicate a percentage of your workers as <b>wall builders</b>. More builders means faster wall construction, but fewer workers available for other buildings.</p>
