<h3>Game Basics</h3>

<h4>Resources</h4>
<p>Your empire produces and consumes several types of resources:</p>
<ul>
    <li><b>Gold</b> - Earned from gold mines, used for training, research, and land conversion</li>
    <li><b>Wood</b> - Produced by woodcutters, used for building, tools, and heating in winter</li>
    <li><b>Food</b> - Produced by farms and hunters, consumed by population and soldiers</li>
    <li><b>Iron</b> - Mined from iron mines, used for tools and weapons</li>
    <li><b>Tools</b> - Created by tool makers, used for construction</li>
    <li><b>Wine</b> - Produced by wineries, used for morale bonuses in battle</li>
</ul>

<h4>Land Types</h4>
<ul>
    <li><b>Plains (P)</b> - Used for farms, houses, forts, markets, and most other buildings</li>
    <li><b>Forest (F)</b> - Used for woodcutters and hunters</li>
    <li><b>Mountains (M)</b> - Used for iron mines and gold mines</li>
</ul>

<h4>Seasons</h4>
<p>The game has summer (months 4-9) and winter (months 10-3). During winter, farms do not produce food, and your people burn wood for heating.</p>

<h4>Population</h4>
<p>Your people work in buildings and can be trained as soldiers. Population grows based on food rationing level and available house space.</p>
