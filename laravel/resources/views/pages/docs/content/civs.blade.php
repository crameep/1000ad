<h3>Civilizations</h3>
<p>Choose one of six civilizations when creating your empire. Each civilization has a unique unit and different bonuses to buildings and soldiers.</p>

<h4>Vikings</h4>
<p>Fierce northern warriors with strong offensive capabilities.</p>
<ul>
    <li><b>Unique Unit:</b> Berserker - Powerful melee fighter with high attack</li>
</ul>

<h4>Franks</h4>
<p>Noble European kingdom with superior cavalry.</p>
<ul>
    <li><b>Unique Unit:</b> Knight - Elite armored horseman</li>
</ul>

<h4>Japanese</h4>
<p>Disciplined warriors with balanced strengths.</p>
<ul>
    <li><b>Unique Unit:</b> Samurai - Skilled swordsman with high defense</li>
</ul>

<h4>Byzantines</h4>
<p>Ancient empire with strong defensive traditions.</p>
<ul>
    <li><b>Unique Unit:</b> Cataphract - Heavily armored cavalry</li>
</ul>

<h4>Mongols</h4>
<p>Nomadic conquerors with fast, mobile armies.</p>
<ul>
    <li><b>Unique Unit:</b> Khan - Swift mounted warrior</li>
</ul>

<h4>Incas</h4>
<p>Mountain empire with strong economy and resilient fighters.</p>
<ul>
    <li><b>Unique Unit:</b> Inca Warrior - Versatile soldier with unique bonuses</li>
</ul>

<p>Each civilization also receives modifiers to specific building outputs and soldier stats. Experiment to find the civilization that best suits your play style.</p>
