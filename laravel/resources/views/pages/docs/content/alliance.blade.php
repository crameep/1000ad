<h3>Alliances</h3>
<p>Join forces with other players by forming or joining an alliance. Alliances provide strategic coordination and shared intelligence.</p>

<h4>Joining or Creating</h4>
<p>You can <b>create</b> a new alliance (you become the leader) or <b>join</b> an existing one using a password provided by the alliance leader.</p>

<h4>Alliance Leader</h4>
<p>The alliance leader can:</p>
<ul>
    <li>Set up to <b>5 ally</b> relations with other alliances</li>
    <li>Declare up to <b>5 war</b> relations with other alliances</li>
    <li>Promote members to <b>trusted</b> status</li>
    <li>Remove members from the alliance</li>
    <li>Set the alliance password</li>
</ul>

<h4>Membership</h4>
<ul>
    <li><b>Starting Members</b> - New members with basic access</li>
    <li><b>Trusted Members</b> - Promoted members with access to more alliance information</li>
</ul>

<h4>Alliance Info</h4>
<p>Members can view alliance-wide statistics, member lists, and diplomatic relations. Coordinate with your alliance for attacks and defense.</p>
