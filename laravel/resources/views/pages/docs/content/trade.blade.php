<h3>Trading</h3>
<p>Use your markets to buy and sell goods. Trading is essential for converting surplus resources into what you need.</p>

<h4>Local Trade</h4>
<p>Buy and sell <b>wood</b>, <b>food</b>, <b>iron</b>, and <b>tools</b> for gold at fixed local market prices. Prices are set by the game and always available.</p>

<h4>Auto-Trade</h4>
<p>Set up recurring trades that execute automatically each turn. Useful for maintaining steady resource flows without manual intervention.</p>

<h4>Global Market</h4>
<p>Trade directly with other players on the global market:</p>
<ul>
    <li><b>Sell</b> - List goods for sale at your chosen price. Other players can buy them.</li>
    <li><b>Buy</b> - Browse and purchase goods listed by other players.</li>
    <li><b>Market Fee</b> - A <b>5% fee</b> is charged on all global market transactions.</li>
    <li><b>Withdrawal Fee</b> - Removing a listing before it sells incurs a <b>10% withdrawal fee</b>.</li>
</ul>

<h4>Market Capacity</h4>
<p>The total volume of goods you can trade is limited by your <b>market buildings</b> and <b>trade research</b> level. Build more markets and research trade capacity to increase your trading volume.</p>
