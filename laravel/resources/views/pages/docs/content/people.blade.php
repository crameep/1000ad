<h3>Population</h3>
<p>Your people are the lifeblood of your empire. They work in buildings, can be trained as soldiers, and grow based on living conditions.</p>

<h4>Population Growth</h4>
<p>Growth is determined by your <b>food rationing</b> level. Higher rationing uses more food but increases growth rate:</p>
<ul>
    <li><b>Very Low</b> - Minimal food, negative growth</li>
    <li><b>Low</b> - Below average growth</li>
    <li><b>Below Normal</b> - Slightly below average growth</li>
    <li><b>Normal</b> - Standard growth rate</li>
    <li><b>Above Normal</b> - Slightly above average growth</li>
    <li><b>High</b> - Above average growth, high food consumption</li>
    <li><b>Very High</b> - Maximum growth rate, very high food consumption</li>
</ul>

<h4>Workers</h4>
<p>Your population works in buildings to produce resources. Each building requires a certain number of workers. Unemployed people do not contribute to production.</p>

<h4>Housing</h4>
<p>Population is limited by available housing space. <b>Houses</b> and <b>town centers</b> provide living space. If housing is full, growth will stop regardless of food rationing.</p>
