<h3>Resources</h3>
<p>Your empire produces and consumes a variety of resources. Managing them effectively is the key to growth and military strength.</p>

<h4>Basic Resources</h4>
<ul>
    <li><b>Wood</b> - Produced by woodcutters. Used for building, tools, heating (winter), and catapults.</li>
    <li><b>Food</b> - Produced by farms (summer) and hunters (year-round). Feeds your population and soldiers.</li>
    <li><b>Iron</b> - Produced by iron mines. Used for tools and weapons.</li>
    <li><b>Gold</b> - Produced by gold mines. Used for training, research, construction, and trade.</li>
</ul>

<h4>Crafted Goods</h4>
<ul>
    <li><b>Tools</b> - Made by tool makers from wood and iron. Required for construction.</li>
    <li><b>Wine</b> - Produced by wineries. Provides morale bonuses in battle.</li>
</ul>

<h4>Military Equipment</h4>
<ul>
    <li><b>Swords</b> - Crafted at weapon smiths. Needed to train swordsmen and horsemen.</li>
    <li><b>Bows</b> - Crafted at weapon smiths. Needed to train archers.</li>
    <li><b>Maces</b> - Crafted at weapon smiths. Needed to train macemen.</li>
    <li><b>Horses</b> - Bred at stables. Needed for horsemen and faster exploration.</li>
</ul>

<h4>Warehouse Capacity</h4>
<p>Resources are stored in <b>warehouses</b> and <b>town centers</b>. If your storage is full, excess production overflows and is lost. Build more warehouses to increase capacity.</p>
