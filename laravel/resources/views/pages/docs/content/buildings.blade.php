<h3>Buildings</h3>
<p>Buildings are the backbone of your empire. Each building uses land and requires workers from your population.</p>

<h4>Resource Buildings</h4>
<ul>
    <li><b>Woodcutter</b> - Produces wood from forest land</li>
    <li><b>Hunter</b> - Produces food from forest land (works year-round)</li>
    <li><b>Farmer</b> - Produces food from plains land (summer only)</li>
    <li><b>Iron Mine</b> - Produces iron from mountain land</li>
    <li><b>Gold Mine</b> - Produces gold from mountain land</li>
</ul>

<h4>Production Buildings</h4>
<ul>
    <li><b>Tool Maker</b> - Converts wood and iron into tools (needed for building)</li>
    <li><b>Weapon Smith</b> - Produces swords, bows, and maces from raw materials</li>
    <li><b>Winery</b> - Produces wine (boosts army morale in battle)</li>
    <li><b>Stable</b> - Produces horses for cavalry</li>
    <li><b>Mage Tower</b> - Produces research points</li>
</ul>

<h4>Support Buildings</h4>
<ul>
    <li><b>House</b> - Provides living space for population</li>
    <li><b>Town Center</b> - Multi-purpose: housing, storage, explorer capacity, trade capacity</li>
    <li><b>Fort</b> - Houses soldiers and increases training capacity</li>
    <li><b>Tower</b> - Provides static defense</li>
    <li><b>Market</b> - Enables trading</li>
    <li><b>Warehouse</b> - Stores goods</li>
</ul>

<h4>Building Status</h4>
<p>Most buildings can be set to operate at 0-100% capacity. Reducing capacity frees up workers but reduces output.</p>
