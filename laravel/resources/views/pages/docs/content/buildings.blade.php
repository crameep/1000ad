<h3>Buildings</h3>
<p>Buildings are the backbone of your empire. Each building uses land and requires workers from your population.</p>

<h4>Resource Buildings</h4>
<ul>
    <li id="B1"><b>Woodcutter</b> - Produces wood from forest land</li>
    <li id="B2"><b>Hunter</b> - Produces food from forest land (works year-round)</li>
    <li id="B3"><b>Farmer</b> - Produces food from plains land (summer only)</li>
    <li id="B5"><b>Iron Mine</b> - Produces iron from mountain land</li>
    <li id="B6"><b>Gold Mine</b> - Produces gold from mountain land</li>
</ul>
<span id="hunters"></span><span id="farms"></span>

<h4>Production Buildings</h4>
<ul>
    <li id="B7"><b>Tool Maker</b> - Converts wood and iron into tools (needed for building)</li>
    <li id="B8"><b>Weapon Smith</b> - Produces swords, bows, and maces from raw materials</li>
    <li id="B16"><b>Winery</b> - Produces wine (boosts army morale in battle)</li>
    <li id="B14"><b>Stable</b> - Produces horses for cavalry</li>
    <li id="B15"><b>Mage Tower</b> - Produces research points</li>
</ul>

<h4>Support Buildings</h4>
<ul>
    <li id="B4"><b>House</b> - Provides living space for population</li>
    <li id="B11"><b>Town Center</b> - Multi-purpose: housing, storage, explorer capacity, trade capacity</li>
    <li id="B9"><b>Fort</b> - Houses soldiers and increases training capacity</li>
    <li id="B10"><b>Tower</b> - Provides static defense</li>
    <li id="B12"><b>Market</b> - Enables trading</li>
    <li id="B13"><b>Warehouse</b> - Stores goods</li>
</ul>

<h4>Building Status</h4>
<p>Most buildings can be set to operate at 0-100% capacity. Reducing capacity frees up workers but reduces output.</p>
