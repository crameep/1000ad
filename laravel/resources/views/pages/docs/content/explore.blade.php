<h3>Exploring</h3>
<p>Send explorers out to discover new land for your empire. More land means more room for buildings and growth.</p>

<h4>How Exploring Works</h4>
<p>Each explorer requires <b>food</b> for the journey. You can optionally equip explorers with <b>horses</b> to increase their speed:</p>
<ul>
    <li><b>On foot</b> - Normal exploration speed (1x)</li>
    <li><b>With 1 horse</b> - Faster exploration (2x speed)</li>
    <li><b>With 2 horses</b> - Fastest exploration (3x speed)</li>
</ul>

<h4>Land Types</h4>
<p>You can choose to seek specific types of land or explore for all types:</p>
<ul>
    <li><b>Mountains</b> - Needed for iron and gold mines</li>
    <li><b>Forest</b> - Needed for woodcutters and hunters</li>
    <li><b>Plains</b> - Needed for farms, houses, forts, and most buildings</li>
    <li><b>All</b> - Accept any type of land found</li>
</ul>

<h4>Explorer Capacity</h4>
<p>The maximum number of explorers you can send at once is determined by your <b>town centers</b>. Build more town centers to send more explorers simultaneously.</p>
