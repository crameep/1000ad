<h3>Welcome to 1000 A.D.</h3>
<p>1000 A.D. is a turn-based medieval strategy game where you build and manage an empire, train armies, research technologies, and compete against other players.</p>

<h4>Getting Started</h4>
<ul>
    <li><b>Build</b> - Construct buildings to produce resources and support your population</li>
    <li><b>Explore</b> - Send explorers to discover new land</li>
    <li><b>Army</b> - Train soldiers to defend your empire and attack others</li>
    <li><b>Research</b> - Advance technology at your mage towers</li>
    <li><b>Trade</b> - Buy and sell goods at the local market</li>
    <li><b>Alliance</b> - Join or create an alliance with other players</li>
</ul>

<h4>Time System</h4>
<p>Each turn represents one month in the game. You accumulate free turns over time, which you can use to advance your empire. The game begins in the year 1000 A.D.</p>

<h4>Protection Period</h4>
<p>New players are protected from attacks for their first 72 turns (6 game years). Use this time to build up your empire!</p>
