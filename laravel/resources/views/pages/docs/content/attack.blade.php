<h3>Attacking</h3>
<p>Send your army, catapults, or thieves on missions against other players. Choose your attack type carefully based on your objectives.</p>

<h4>Army Attacks</h4>
<ul>
    <li><b>Conquer</b> - Capture land from the defender</li>
    <li><b>Raid</b> - Steal resources from the defender</li>
    <li><b>Rob</b> - Steal gold specifically</li>
    <li><b>Slaughter</b> - Kill the defender's population</li>
</ul>

<h4>Catapult Attacks</h4>
<ul>
    <li><b>Target Army</b> - Destroy enemy soldiers</li>
    <li><b>Target Population</b> - Kill enemy civilians</li>
    <li><b>Target Buildings</b> - Destroy enemy structures</li>
</ul>

<h4>Thief Missions</h4>
<ul>
    <li><b>Steal Info</b> - Spy on the target's empire details</li>
    <li><b>Steal Goods</b> - Steal resources covertly</li>
    <li><b>Poison</b> - Poison the target's food supply</li>
    <li><b>Set Fire</b> - Burn down buildings</li>
</ul>

<h4>Attack Mechanics</h4>
<p>Attack power is determined by the number and type of units sent. <b>Wine</b> provides a morale bonus to your attacking forces. Repeated attacks against the same target suffer <b>diminishing returns</b>.</p>

<h4>Protection & Revolts</h4>
<p>New players are protected for their first <b>72 turns</b>. Attacking a player whose score is less than <b>50% of yours</b> may trigger a revolt warning in your empire.</p>
