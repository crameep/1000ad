<h3>Sending Aid</h3>
<p>Send resources to other players to help allies or fulfill trade agreements.</p>

<h4>How Aid Works</h4>
<ul>
    <li>You can send any combination of resources to another player</li>
    <li>A <b>5% fee</b> is deducted from the resources sent</li>
    <li>Aid shipments take <b>3 turns</b> to arrive at the destination</li>
    <li>The recipient receives the resources automatically when the shipment arrives</li>
</ul>

<h4>Requirements</h4>
<p>Sending aid requires available <b>market capacity</b>. Build more markets and research trade capacity to increase the volume of aid you can send.</p>

<h4>Strategy</h4>
<p>Aid is a key tool for alliance coordination. Send resources to allies who are under attack, or help new alliance members get established. Plan ahead since shipments take 3 turns to arrive.</p>
