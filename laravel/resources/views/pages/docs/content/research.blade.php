<h3>Research</h3>
<p>Advance your empire's technology by researching upgrades at your mage towers. Research points are produced each turn by your mage towers.</p>

<h4>Research Categories</h4>

<p><b>Military</b></p>
<ul>
    <li>Attack Strength - Increases army attack power</li>
    <li>Defense Strength - Increases defensive power</li>
    <li>Espionage - Improves thief success rate</li>
    <li>Counter-Espionage - Improves thief detection</li>
</ul>

<p><b>Production</b></p>
<ul>
    <li>Mining Efficiency - Iron and gold mines produce more</li>
    <li>Farming Efficiency - Farms and hunters produce more</li>
    <li>Forestry Efficiency - Woodcutters produce more</li>
    <li>Tool Production - Tool makers produce more</li>
</ul>

<p><b>Other</b></p>
<ul>
    <li>Trade Capacity - Increases market capacity</li>
    <li>Exploration - Explorers find more land</li>
    <li>Construction - Build more per turn</li>
    <li>Population Growth - Faster population growth</li>
</ul>

<h4>Research Cost</h4>
<p>Each level costs research points based on the formula: <b>10 + totalLevels&sup2; &times; &radic;totalLevels</b>, where totalLevels is the sum of all your research levels. Costs increase exponentially, so plan your priorities carefully.</p>
